package school;

public class VariousLoops {

	public static void main(String[] args) {
		// For loop
		for(int i=1;i<=5;i++) {
		System.out.println(i);

		}
		//While Loop
		int j=1;
		while(j<=10) {
			System.out.println("\t"+j);
			j++;
		}
		//Do While loop
		int k=1;
		do {
			System.out.println("\t\t"+k);
			k++;
		}
		while(k<=5);

		
	}
	
}
